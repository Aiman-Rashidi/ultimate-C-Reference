#include <stdio.h>

int main()
{

    int i = 0;
    while (i < 10)
    {
        printf("The value of i is %d.\n", i);
        i++;
    }

    printf("\n\n");

    int j = 1;
    while (j < 10)
    {
        printf("The value of j is %d.\n", j);
        j++;
    }

    printf("\n\n");

    int k = -10;
    while (k < 10)
    {
        printf("The value of j is %d.\n", k);
        k++;
    }

    return 0;
}